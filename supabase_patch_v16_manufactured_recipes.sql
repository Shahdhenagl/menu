FROM _recipe r LEFT JOIN inventory_items i ON i.name = r.ing WHERE i.id IS NULL
UNION ALL
SELECT 'منتج غير مطابق', r.prod
FROM _recipe r LEFT JOIN inventory_items m ON m.name = r.prod WHERE m.id IS NULL;
