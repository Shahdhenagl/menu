-- (3) إدخال الوصفات في manufacturing_recipes (مع منع التكرار)
INSERT INTO manufacturing_recipes (manufactured_item_id, ingredient_item_id, quantity)
SELECT m.id, i.id, r.qty
FROM _recipe r
JOIN inventory_items m ON m.name = r.prod
JOIN inventory_items i ON i.name = r.ing
WHERE NOT EXISTS (
  SELECT 1 FROM manufacturing_recipes mr
  WHERE mr.manufactured_item_id = m.id AND mr.ingredient_item_id = i.id
);

