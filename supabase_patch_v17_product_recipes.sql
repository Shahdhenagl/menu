-- (4) مسح الوصفات القديمة للمنتجات المتطابقة فقط (عشان تتحدّث بنسخة الشيت بالكامل)
DELETE FROM product_recipes pr
USING products p
WHERE pr.product_id = p.id
  AND fn_norm_ar(p.name_ar) IN (SELECT DISTINCT fn_norm_ar(prod) FROM _pr);

-- (5) إدخال الوصفات: ربط المنتج بالصنف + جمع الكميات المكررة لنفس الصنف
INSERT INTO product_recipes (product_id, inventory_item_id, quantity)
SELECT p.id, i.id, SUM(r.qty)
FROM _pr r
JOIN products p        ON fn_norm_ar(p.name_ar) = fn_norm_ar(r.prod)
JOIN inventory_items i ON fn_norm_ar(i.name)    = fn_norm_ar(r.ing)
GROUP BY p.id, i.id
ON CONFLICT (product_id, inventory_item_id) DO UPDATE SET quantity = EXCLUDED.quantity;

COMMIT;

