-- ============================================================================

-- 6.أ) ملخّص الربط:
SELECT
  (SELECT count(DISTINCT prod) FROM _pr) AS منتجات_الشيت,
  (SELECT count(DISTINCT p.id) FROM _pr r JOIN products p ON fn_norm_ar(p.name_ar)=fn_norm_ar(r.prod)) AS اترطبت_فعلاً,
  (SELECT count(*) FROM product_recipes) AS اجمالي_اسطر_الوصفات;

-- 6.ب) ✅ مكوّنات ملقتش لها صنف مخزون (المفروض ترجع صفر صفوف):
SELECT DISTINCT r.ing AS مكوّن_غير_موجود_في_المخزون
FROM _pr r
LEFT JOIN inventory_items i ON fn_norm_ar(i.name) = fn_norm_ar(r.ing)
WHERE i.id IS NULL
ORDER BY 1;

-- 6.ج) ⚠️ الأهم: منتجات في الشيت ملقتش لها اسم مطابق على الويب سايت
--      (لو طلعت صفوف: ده معناه إن الاسم على الويب سايت مختلف — عدّله أو ابعتهولي وأنا أظبط المطابقة):
SELECT DISTINCT r.prod AS منتج_غير_موجود_على_الويب_سايت
FROM _pr r
LEFT JOIN products p ON fn_norm_ar(p.name_ar) = fn_norm_ar(r.prod)
WHERE p.id IS NULL
ORDER BY 1;

